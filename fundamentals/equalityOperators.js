// 4 Equality Operators
// == - Loose Equality
// === - Strict Equality
// != - Not Equal
// !== - Strictly Not Equal

let a = 200
let b = "200"

console.log('Loose Equality:    a == b  =>', a == b)
console.log('Strict Equality:   a === b =>', a === b)
console.log('InEquality:        a != b  =>', a != b)
console.log('Strict InEquality: a !== b =>', a !== b)