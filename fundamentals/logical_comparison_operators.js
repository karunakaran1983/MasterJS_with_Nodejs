// logical Operators
let a = true
let b = false
console.log('Logical AND - a && b        => ', a && b)
console.log('Logical OR  - a || b        => ', a || b)
console.log('Logical NOT - !a            => ', !a)
console.log('Logical AND & NOT - a && !b => ', a && !b)
console.log('Logical OR & NOT - !a || !b => ', !a || !b)

// Comparison Operators
let x = 100
let y = 200
console.log('Comparison > - x > y        => ', x > y)
console.log('Comparison < - x < y        => ', x < y)
console.log('Comparison >= - x >= y      => ', x >= y)
console.log('Comparison <= - x <= y      => ', x <= y)